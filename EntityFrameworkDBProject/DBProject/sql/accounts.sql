CREATE TABLE ACCOUNTS
(
	ACCOUNT_ID SERIAL PRIMARY KEY,
	ACCOUNT_NUMBER INTEGER NOT NULL,
	CLIENT_ID INTEGER REFERENCES CLIENTS (CLIENT_ID) NOT NULL,
	PRODUCT_ID INTEGER REFERENCES PRODUCTS (PRODUCT_ID) NOT NULL,
	CURRENCY INTEGER REFERENCES DICT_CURRENCY (ID_CODE) NOT NULL,
	OPENING_DATE DATE NOT NULL DEFAULT CURRENT_DATE,
	CLOSING_DATE DATE DEFAULT
);

COMMENT ON TABLE ACCOUNTS IS 'Счета';
COMMENT ON COLUMN ACCOUNTS.ACCOUNT_ID IS 'Идентификатор счета';
COMMENT ON COLUMN ACCOUNTS.ACCOUNT_NUMBER IS 'Номер счета';
COMMENT ON COLUMN ACCOUNTS.CLIENT_ID IS 'Идентификатор клиента';
COMMENT ON COLUMN ACCOUNTS.PRODUCT_ID IS 'Идентификатор продукта';
COMMENT ON COLUMN ACCOUNTS.CURRENCY IS 'Валюта счета';
COMMENT ON COLUMN ACCOUNTS.OPENING_DATE IS 'Дата открытия счета';
COMMENT ON COLUMN ACCOUNTS.CLOSING_DATE IS 'Дата закрытия счета';

